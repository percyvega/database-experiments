/* Create the database */
CREATE DATABASE IF NOT EXISTS classic_models;

/* Switch to the classic_models database */
USE classic_models;

/* Drop existing tables  */
DROP TABLE IF EXISTS order_details;
DROP TABLE IF EXISTS orders;
DROP TABLE IF EXISTS payments;
DROP TABLE IF EXISTS employees;
DROP TABLE IF EXISTS customers;
DROP TABLE IF EXISTS offices;
DROP TABLE IF EXISTS products;
DROP TABLE IF EXISTS product_lines;


/* Create the tables */
CREATE TABLE IF NOT EXISTS
  product_lines (
    productLine varchar(50),
    textDescription varchar(4000) DEFAULT NULL,
    htmlDescription mediumtext,
    image mediumblob,
    PRIMARY KEY (productLine)
  );

CREATE TABLE IF NOT EXISTS
  products (
    productCode varchar(15),
    productName varchar(70) NOT NULL,
    productLine varchar(50) NOT NULL,
    productScale varchar(10) NOT NULL,
    productVendor varchar(50) NOT NULL,
    productDescription text NOT NULL,
    quantityInStock smallint NOT NULL,
    buyPrice decimal(10, 2) NOT NULL,
    MSRP decimal(10, 2) NOT NULL,
    PRIMARY KEY (productCode),
    FOREIGN KEY (productLine) REFERENCES product_lines (productLine)
  );

CREATE TABLE IF NOT EXISTS
  offices (
    officeCode varchar(10),
    city varchar(50) NOT NULL,
    phone varchar(50) NOT NULL,
    addressLine1 varchar(50) NOT NULL,
    addressLine2 varchar(50) DEFAULT NULL,
    state varchar(50) DEFAULT NULL,
    country varchar(50) NOT NULL,
    postalCode varchar(15) NOT NULL,
    territory varchar(10) NOT NULL,
    PRIMARY KEY (officeCode)
  );

CREATE TABLE IF NOT EXISTS
  customers (
    customerNumber int,
    customerName varchar(50) NOT NULL,
    contactLastName varchar(50) NOT NULL,
    contactFirstName varchar(50) NOT NULL,
    phone varchar(50) NOT NULL,
    addressLine1 varchar(50) NOT NULL,
    addressLine2 varchar(50) DEFAULT NULL,
    city varchar(50) NOT NULL,
    state varchar(50) DEFAULT NULL,
    postalCode varchar(15) DEFAULT NULL,
    country varchar(50) NOT NULL,
    creditLimit decimal(10, 2) DEFAULT NULL,
    PRIMARY KEY (customerNumber)
  );

CREATE TABLE IF NOT EXISTS
  employees (
    employeeNumber int,
    lastName varchar(50) NOT NULL,
    firstName varchar(50) NOT NULL,
    extension varchar(10) NOT NULL,
    email varchar(100) NOT NULL,
    officeCode varchar(10) NULL,
    reportsTo int DEFAULT NULL,
    supportsCustomerNumber int DEFAULT NULL,
    jobTitle varchar(50) NOT NULL,
    PRIMARY KEY (employeeNumber),
    FOREIGN KEY (reportsTo) REFERENCES employees (employeeNumber),
    FOREIGN KEY (officeCode) REFERENCES offices (officeCode),
    FOREIGN KEY (supportsCustomerNumber) REFERENCES customers (customerNumber)
  );

CREATE TABLE IF NOT EXISTS
  payments (
    customerNumber int,
    checkNumber varchar(50) NOT NULL,
    paymentDate date NOT NULL,
    amount decimal(10, 2) NOT NULL,
    PRIMARY KEY (customerNumber, checkNumber),
    FOREIGN KEY (customerNumber) REFERENCES customers (customerNumber)
  );

CREATE TABLE IF NOT EXISTS
  orders (
    orderNumber int,
    orderDate date NOT NULL,
    requiredDate date NOT NULL,
    shippedDate date DEFAULT NULL,
    status varchar(15) NOT NULL,
    comments text,
    customerNumber int NOT NULL,
    PRIMARY KEY (orderNumber),
    FOREIGN KEY (customerNumber) REFERENCES customers (customerNumber)
  );

CREATE TABLE IF NOT EXISTS
  order_details (
    orderNumber int,
    productCode varchar(15) NOT NULL,
    quantityOrdered int NOT NULL,
    priceEach decimal(10, 2) NOT NULL,
    orderLineNumber smallint NOT NULL,
    PRIMARY KEY (orderNumber, productCode),
    FOREIGN KEY (orderNumber) REFERENCES orders (orderNumber),
    FOREIGN KEY (productCode) REFERENCES products (productCode)
  );

